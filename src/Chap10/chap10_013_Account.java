package Chap10;					// 예외처리 // 사용자 정의 예외 발생시키기

public class chap10_013_Account {
	private long balance;
	
	public chap10_013_Account()	{}
	
	public long getBalance()	{
		return balance;
	}
	public void deposit(int money)	{
		balance += money;
	}
	public void withdraw(int money) throws chap10_012_BalanceInsufficientException {					// 사용자 정의 예외 떠넘기기
		if(balance < money) {
			throw new chap10_012_BalanceInsufficientException("잔고부족:"+(money-balance)+" 모자람");		// 사용자 정의 예외 발생
		}
		balance -= money;
	}
	
}
